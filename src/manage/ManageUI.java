    private boolean validateFields()
     {
        if(stid.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(ManageUI.this, "Enter store ID");
            stid.grabFocus();
            return false;
        }
        if(type.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(ManageUI.this, "Enter store type");
            type.grabFocus();
            return false;
        }
        if(desc.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(ManageUI.this, "Enter description");
            desc.grabFocus();
            return false;
        }
        return true;
    }
    
    private int getSerialNumber()
    {
        try
        {
            Connection con =MyConnection.getConnection();
            PreparedStatement ps=con.prepareStatement("select Store_id from storetype");
            ResultSet rs=ps.executeQuery();
            int i=1;
           // int sno=1;
            
            while(rs.next())
            {
                
                int sn=Integer.parseInt(rs.getString("Store_id"));
                
                if(sn!=i)
                {
                    return i;
                   
                }
               
                i++;
                
            }
            
            return i;
            
        }
        catch(Exception e)
        {
            System.out.println(e);
            return 0;
        }
        
        
    }

