    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String typ=type.getText();
        String des=desc.getText();
        String sid= stid.getText();
        if(validateFields())
        {
            try
            {
                int code=JOptionPane.showConfirmDialog(this,"Are you sure you want to update this information?","Confirm",JOptionPane.YES_NO_OPTION);
                if(code==JOptionPane.YES_OPTION)
                {
                    
                    Connection con =MyConnection.getConnection();
                    String sql="update storetype set store_name= '"+typ+"', description = '"+ des+"' where store_id ="+ sid;
                    PreparedStatement ps=con.prepareStatement(sql);
                   
                    if(ps.executeUpdate()>0)
                    {
                        JOptionPane.showMessageDialog(this,"Updation Successful!!","success",JOptionPane.INFORMATION_MESSAGE);
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this,"Incorect values entered");
                    }
                }
            }
            catch(Exception e)
            {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

