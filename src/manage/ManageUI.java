    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String ty=type.getText();
        String de=desc.getText();
        String si=null;
        if(validateFields())
        {
        try
        {
            Connection con =MyConnection.getConnection();
            PreparedStatement ps=con.prepareStatement("insert into storetype values(?,?,?)");
            
            if(!(si=(String.valueOf(getSerialNumber()))).equals("0"))
                { 
                    ps.setString(1,si); 
                }
            ps.setString(2,ty);
            ps.setString(3,de);
            if(ps.executeUpdate()>0)
            {
                JOptionPane.showMessageDialog(this,"StoreType Succcessfully Added");
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

